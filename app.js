var isCollision = function(type, selfX, selfY){ // 1-right 2-down 3-left 4-up 5-bullet
	var posX = selfX;
	var posY = selfY;
	for (var i in walls){
		var wall = walls[i];
		
		if (type === 1 &&
			posX+5 + 3 > wall.x*10 && 
			posX-5 + 3 < wall.x*10 + 10 && 
			posY+5 > wall.y*10 &&
			posY-5 < wall.y*10 + 10
		){
			return true;
		}
		else if (type === 2 &&
			posX+5 > wall.x*10 && 
			posX-5 < wall.x*10 + 10 && 
			posY+5 + 3 > wall.y*10 &&
			posY-5 + 3 < wall.y*10 + 10
		){
			return true;
		}
		else if (type === 3 &&
			posX+5 - 3 > wall.x*10 && 
			posX-5 - 3 < wall.x*10 + 10 && 
			posY+5 > wall.y*10 &&
			posY-5 < wall.y*10 + 10
		){
			return true;
		}
		else if (type === 4 &&
			posX+5 > wall.x*10 && 
			posX-5 < wall.x*10 + 10 && 
			posY+5 - 3> wall.y*10 &&
			posY-5 - 3< wall.y*10 + 10
		){
			return true;
		}
		else if (type === 5 &&
			posX > wall.x*10 && 
			posX < wall.x*10 + 10 && 
			posY > wall.y*10 &&
			posY < wall.y*10 + 10
		){
			return true;
		}
	}
}
	
